-- Object Type: PROCEDURES
CREATE OR REPLACE PROCEDURE ALFA_EDW_DEV.PUBLIC.M_LOAD_STG_FARMHVYM("RUN_ID" VARCHAR)
RETURNS VARCHAR
LANGUAGE SQL
EXECUTE AS CALLER
AS ' BEGIN 

-- Component SQ_FARMHVYM, Type Pre SQL 
DELETE  FROM DB_T_STAG_MEMBXREF_PROD.STG_FARMHVYM where load_dt in
(select distinct a.load_dt from	DB_T_STAG_MEMBXREF_PROD.STG_FARMHVYM A
	,DB_T_STAG_MEMBXREF_PROD.STG_MEMBXREF_TRIGGER B
WHERE
	A.LOAD_DT = B.FEED_DT
	AND B.TABLENAME = ''FARMHVYM'');


-- Component SQ_FARMHVYM, Type SOURCE 
CREATE OR REPLACE TEMPORARY TABLE SQ_FARMHVYM AS
(
SELECT /* adding column aliases to ensure proper downstream column references */
$1 as SC_POLICY_NUM,
$2 as RFD_HVYM_NDX,
$3 as RFD_HVYM_YR,
$4 as RFD_HVYM_DESC,
$5 as RFD_HVYM_DESC2,
$6 as RFD_HVYM_ID,
$7 as RFD_HVYM_ITM_COV,
$8 as FEED_DT,
$9 as FEED_IND,
$10 as source_record_id
FROM (
SELECT SRC.*, row_number() over (order by 1) AS source_record_id FROM (
SELECT 	
	SC_POLICY_NUM ,
      RFD_HVYM_NDX ,
      RFD_HVYM_YR,
      RFD_HVYM_DESC,
      RFD_HVYM_DESC2,
      RFD_HVYM_ID,
      RFD_HVYM_ITM_COV
	,B.FEED_DT,B.FEED_IND
FROM 
	DB_T_STAG_MEMBXREF_PROD.FARMHVYM A
	JOIN
	DB_T_STAG_MEMBXREF_PROD.STG_MEMBXREF_TRIGGER B
	ON 1=1
WHERE
	TABLENAME = ''FARMHVYM''
) SRC
)
);


-- Component Exp_Trans, Type EXPRESSION 
CREATE OR REPLACE TEMPORARY TABLE Exp_Trans AS
(
SELECT
SQ_FARMHVYM.SC_POLICY_NUM as SC_POLICY_NUM,
SQ_FARMHVYM.RFD_HVYM_NDX as RFD_HVYM_NDX,
SQ_FARMHVYM.RFD_HVYM_YR as RFD_HVYM_YR,
SQ_FARMHVYM.RFD_HVYM_DESC as RFD_HVYM_DESC,
SQ_FARMHVYM.RFD_HVYM_DESC2 as RFD_HVYM_DESC2,
SQ_FARMHVYM.RFD_HVYM_ID as RFD_HVYM_ID,
SQ_FARMHVYM.RFD_HVYM_ITM_COV as RFD_HVYM_ITM_COV,
TO_DATE ( SQ_FARMHVYM.FEED_DT  ) as o_FEED_DT,
SQ_FARMHVYM.FEED_IND as FEED_IND,
SQ_FARMHVYM.source_record_id
FROM
SQ_FARMHVYM
);


-- Component STG_FARMHVYM, Type TARGET 
INSERT INTO DB_T_STAG_MEMBXREF_PROD.STG_FARMHVYM
(
SC_POLICY_NUM,
RFD_HVYM_NDX,
RFD_HVYM_YR,
RFD_HVYM_DESC,
RFD_HVYM_DESC2,
RFD_HVYM_ID,
RFD_HVYM_ITM_COV,
LOAD_DT,
FEED_IND
)
SELECT
Exp_Trans.SC_POLICY_NUM as SC_POLICY_NUM,
Exp_Trans.RFD_HVYM_NDX as RFD_HVYM_NDX,
Exp_Trans.RFD_HVYM_YR as RFD_HVYM_YR,
Exp_Trans.RFD_HVYM_DESC as RFD_HVYM_DESC,
Exp_Trans.RFD_HVYM_DESC2 as RFD_HVYM_DESC2,
Exp_Trans.RFD_HVYM_ID as RFD_HVYM_ID,
Exp_Trans.RFD_HVYM_ITM_COV as RFD_HVYM_ITM_COV,
Exp_Trans.o_FEED_DT as LOAD_DT,
Exp_Trans.FEED_IND as FEED_IND
FROM
Exp_Trans;


END; ';