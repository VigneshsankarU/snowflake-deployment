-- Object Type: PROCEDURES
CREATE OR REPLACE PROCEDURE ALFA_EDW_DEV.PUBLIC.M_STAG_FARMMACH("RUN_ID" VARCHAR)
RETURNS VARCHAR
LANGUAGE SQL
EXECUTE AS CALLER
AS ' DECLARE FEED_IND varchar;
FEED_DT date;
BEGIN 

FEED_IND:=(select DAILY_FEED_IND from DB_T_CTRL_PROD.ECTL_JOB_LOAD_STATUS_LOG where ECTL_BATCH_ID= :run_id);  
FEED_DT:=(select current_date);  

-- Component FARMMACH2, Type TRUNCATE_TABLE 
TRUNCATE TABLE DB_T_STAG_MEMBXREF_PROD.FARMMACH;


-- PIPELINE START FOR 1

-- Component SQ_FARMMACH3, Type SOURCE 
CREATE OR REPLACE TEMPORARY TABLE SQ_FARMMACH3 AS
(
SELECT /* adding column aliases to ensure proper downstream column references */
$1 as record_Count,
$2 as source_record_id
FROM (
SELECT SRC.*, row_number() over (order by 1) AS source_record_id FROM (
SELECT count(*) as record_count
FROM
DB_T_STAG_MEMBXREF_PROD.FARMMACH
) SRC
)
);


-- Component exp_rec_cnt, Type EXPRESSION 
CREATE OR REPLACE TEMPORARY TABLE exp_rec_cnt AS
(
SELECT
SQ_FARMMACH3.record_Count as record_count,
:feed_ind as feed_ind,
:feed_dt as feed_dt,
SQ_FARMMACH3.source_record_id
FROM
SQ_FARMMACH3
);


-- Component TRG_MEMBXREF, Type TARGET_EXPORT_PREPARE Stage data before exporting
CREATE OR REPLACE TEMPORARY TABLE TRG_MEMBXREF AS
(
SELECT
exp_rec_cnt.feed_ind as feed_ind,
exp_rec_cnt.feed_dt as feed_dt,
exp_rec_cnt.record_count as record_cnt
FROM
exp_rec_cnt
);


-- Component TRG_MEMBXREF, Type EXPORT_DATA Exporting data
;


-- PIPELINE END FOR 1

-- PIPELINE START FOR 2

-- Component SQ_FARMMACH2, Type SOURCE 
CREATE OR REPLACE TEMPORARY TABLE SQ_FARMMACH2 AS
(
SELECT /* adding column aliases to ensure proper downstream column references */
$1 as SC_POLICY_NUM,
$2 as RFD_MACH_NDX,
$3 as RFD_MACH_YR,
$4 as RFD_MACH_DESC,
$5 as RFD_MACH_DESC2,
$6 as RFD_MACH_ID,
$7 as RFD_MACH_ITM_COV,
$8 as source_record_id
FROM (
SELECT SRC.*, row_number() over (order by 1) AS source_record_id FROM (
SELECT
FARMMACH.SC_POLICY_NUM,
FARMMACH.RFD_MACH_NDX,
FARMMACH.RFD_MACH_YR,
FARMMACH.RFD_MACH_DESC,
FARMMACH.RFD_MACH_DESC2,
FARMMACH.RFD_MACH_ID,
FARMMACH.RFD_MACH_ITM_COV
FROM DB_T_STAG_MEMBXREF_PROD.FARMMACH
) SRC
)
);


-- Component exp_pass_through, Type EXPRESSION 
CREATE OR REPLACE TEMPORARY TABLE exp_pass_through AS
(
SELECT
SQ_FARMMACH2.SC_POLICY_NUM as SC_POLICY_NUM,
SQ_FARMMACH2.RFD_MACH_NDX as RFD_MACH_NDX,
SQ_FARMMACH2.RFD_MACH_YR as RFD_MACH_YR,
SQ_FARMMACH2.RFD_MACH_DESC as RFD_MACH_DESC,
SQ_FARMMACH2.RFD_MACH_DESC2 as RFD_MACH_DESC2,
SQ_FARMMACH2.RFD_MACH_ID as RFD_MACH_ID,
SQ_FARMMACH2.RFD_MACH_ITM_COV as RFD_MACH_ITM_COV,
SQ_FARMMACH2.source_record_id
FROM
SQ_FARMMACH2
);


-- Component FARMMACH2, Type TARGET 
INSERT INTO DB_T_STAG_MEMBXREF_PROD.FARMMACH
(
SC_POLICY_NUM,
RFD_MACH_NDX,
RFD_MACH_YR,
RFD_MACH_DESC,
RFD_MACH_DESC2,
RFD_MACH_ID,
RFD_MACH_ITM_COV
)
SELECT
exp_pass_through.SC_POLICY_NUM as SC_POLICY_NUM,
exp_pass_through.RFD_MACH_NDX as RFD_MACH_NDX,
exp_pass_through.RFD_MACH_YR as RFD_MACH_YR,
exp_pass_through.RFD_MACH_DESC as RFD_MACH_DESC,
exp_pass_through.RFD_MACH_DESC2 as RFD_MACH_DESC2,
exp_pass_through.RFD_MACH_ID as RFD_MACH_ID,
exp_pass_through.RFD_MACH_ITM_COV as RFD_MACH_ITM_COV
FROM
exp_pass_through;


-- PIPELINE END FOR 2

END; ';