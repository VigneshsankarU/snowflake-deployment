RFD_E_DESC,
LOAD_DT,
FEED_IND
)
SELECT
EXPTRANS.SC_POLICY_NUM as SC_POLICY_NUM,
EXPTRANS.RFD_OUTBLD_NDX as RFD_OUTBLD_NDX,
EXPTRANS.RFD_ECLASS as RFD_ECLASS,
EXPTRANS.RFD_ECOV as RFD_ECOV,
EXPTRANS.RFD_PCLASS as RFD_PCLASS,
EXPTRANS.RFD_PECOV as RFD_PECOV,
EXPTRANS.RFD_E_DT as RFD_E_DT,
EXPTRANS.RFD_E_PREM as RFD_E_PREM,
EXPTRANS.RFD_E_DED as RFD_E_DED,
EXPTRANS.RFD_PE_DED as RFD_PE_DED,
EXPTRANS.RFD_E_CNST_YR as RFD_E_CNST_YR,
EXPTRANS.RFD_E_KBRACE as RFD_E_KBRACE,
EXPTRANS.RFD_PE_KBRACE as RFD_PE_KBRACE,
EXPTRANS.RFD_E_CNST_TY as RFD_E_CNST_TY,
EXPTRANS.RFD_E_DESC as RFD_E_DESC,
EXPTRANS.B_FEED_DATE as LOAD_DT,
EXPTRANS.B_FEED_IND as FEED_IND
FROM
