-- Component SQ_STG_MEMBXREF_TRIGGER, Type Pre SQL 
DELETE  FROM DB_T_STAG_MEMBXREF_PROD.STG_FARMPOLCLM where load_dt in
  ( select distinct a.load_dt from  DB_T_STAG_MEMBXREF_PROD.STG_FARMPOLCLM A
    ,DB_T_STAG_MEMBXREF_PROD.STG_MEMBXREF_TRIGGER B
WHERE
    A.LOAD_DT = B.FEED_DT
